    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
    try {
        String sql = "select * from login where Username='"+txtuser.getText()+"' and Password='"+txtpw.getText()+"' and HakAkses='"+cmbhak.getSelectedItem()+"'" ;
         pst = konek.prepareStatement(sql);
         rst = pst.executeQuery();
         if (rst.next()) {
             if (txtuser.getText().equals(rst.getString("Username")) &&txtpw.getText().equals(rst.getString("Password")) &&cmbhak.getSelectedItem().equals(rst.getString("HakAkses")))
                if (cmbhak.getSelectedItem().equals("Admin")){
                        register.setEnabled(true);
                        logout.setEnabled(true);
                        master.setEnabled(true);
                        transaksi.setEnabled(true);
                        laporan.setEnabled(true);
                        txtTanggal.setEnabled(false);
                        txtJam.setEnabled(false);
                        FormLogin.setVisible(false);
                        btnregister.setEnabled(true);
                        btnpelanggan.setEnabled(true);
                        btnproduk.setEnabled(true);
                        btnpenjualan.setEnabled(true);        
                 } else if (cmbhak.getSelectedItem().equals("Petugas")){  
                        register.setEnabled(false);
                        logout.setEnabled(true);
                        transaksi.setEnabled(true);
                        master.setEnabled(true);
                        transaksi.setEnabled(true);
                        laporan.setEnabled(true);
                        txtTanggal.setEnabled(true);
                        txtJam.setEnabled(true);
                        FormLogin.setVisible(true);
                        btnregister.setEnabled(false);
                        btnpelanggan.setEnabled(true);
                        btnproduk.setEnabled(true);
                        btnpenjualan.setEnabled(true);
         }
             {
                JOptionPane.showMessageDialog(null, "Selamat Datang '"+txtuser.getText()+"'");
                    String text = txtuser.getText();
                    lblUser.setText(" Selamat Datang, "+text);
        }
        } else {
                JOptionPane.showMessageDialog(null, "Gagal Masuk, Periksa Kembali");
                    txtuser.setText("");
                    txtpw.setText("");
                    cmbhak.setSelectedItem("-Pilih Salah Satu");
}

         } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Login Gagal");
      
}
       
    }//GEN-LAST:event_btnloginActionPerformed

